//Write a function min(a,b) which returns the least of two numbers a and b.
function min(a,b){
  return a>b ? b:a;
}
console.log(min(1,-1))

//Write a function pow(x,n) that returns x in power n. Or, in other words, multiplies x by itself n times and returns the result.
function pow(x,n){
    return x**n
}
console.log(pow(3,2))